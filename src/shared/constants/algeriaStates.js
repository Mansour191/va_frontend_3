/**
 * Get state by ID
 * @param {number} id - State ID (1-58)
 * @returns {Object|null} State object or null if not found
 */
export const getStateById = (id) => {
  return ALGERIA_STATES.find(state => state.id === id) || null;
};

/**
 * Get state by code
 * @param {string} code - State code (01-58)
 * @returns {Object|null} State object or null if not found
 */
export const getStateByCode = (code) => {
  return ALGERIA_STATES.find(state => state.code === code) || null;
};

/**
 * Get states formatted for select dropdown
 * @param {string} language - Language preference ('ar', 'fr', 'en')
 * @returns {Array} Array of {value, label} objects
 */
export const getStatesForSelect = (language = 'ar') => {
  return ALGERIA_STATES.map(state => ({
    value: state.id,
    code: state.code,
    label: state[`name${language === 'fr' ? 'Fr' : language === 'en' ? '' : 'Ar'}`] || state.nameAr,
    name: state.name,
    nameAr: state.nameAr,
    nameFr: state.nameFr
  }));
};

/**
 * Search states by name
 * @param {string} query - Search query
 * @param {string} language - Language preference
 * @returns {Array} Array of matching states
 */
export const searchStates = (query, language = 'ar') => {
  if (!query) return ALGERIA_STATES;
  
  const searchField = language === 'fr' ? 'nameFr' : 'nameAr';
  return ALGERIA_STATES.filter(state => 
    state[searchField].toLowerCase().includes(query.toLowerCase()) ||
    state.name.toLowerCase().includes(query.toLowerCase()) ||
    state.code.includes(query)
  );
};

/**
 * Get states by region (Northern, Southern, etc.)
 * @param {string} region - Region name
 * @returns {Array} Array of states in the region
 */
